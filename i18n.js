  let currentLang = "en";

  function resolveLanguage(stored) {
    if (stored && stored !== "auto" && MESSAGES[stored]) {
      return stored;
    }

    const raw = (navigator.language || "en").toLowerCase().replace("_", "-");
    const alias = LOCALE_ALIASES[raw];
    if (alias && MESSAGES[alias]) {
      return alias;
    }
    if (MESSAGES[raw]) {
      return raw;
    }

    const base = raw.split("-")[0];
    const baseAlias = LOCALE_ALIASES[base];
    if (baseAlias && MESSAGES[baseAlias]) {
      return baseAlias;
    }
    return MESSAGES[base] ? base : "en";
  }

  function t(key, lang) {
    const resolved = lang || currentLang;
    return MESSAGES[resolved]?.[key] ?? MESSAGES.en[key] ?? key;
  }

  function applyTranslations(lang) {
    currentLang = lang;
    document.documentElement.lang = lang;
    document.documentElement.dir = RTL_LANGUAGES.has(lang) ? "rtl" : "ltr";

    document.querySelectorAll("[data-i18n]").forEach((el) => {
      el.textContent = t(el.dataset.i18n, lang);
    });

    document.querySelectorAll("[data-i18n-title]").forEach((el) => {
      el.title = t(el.dataset.i18nTitle, lang);
    });

    const titleKey = document.body.dataset.i18nTitle;
    if (titleKey) {
      document.title = t(titleKey, lang);
    }
  }

  function sortedLanguageEntries() {
    return Object.entries(SUPPORTED_LANGUAGES).sort((a, b) => {
      if (a[0] === "en") return -1;
      if (b[0] === "en") return 1;
      return a[1].localeCompare(b[1], undefined, { sensitivity: "base" });
    });
  }

  function populateLanguageSelect(select, storedLanguage, lang) {
    const displayLang = lang || currentLang;
    select.innerHTML = "";

    for (const [code, name] of sortedLanguageEntries()) {
      const option = document.createElement("option");
      option.value = code;
      option.textContent = name;
      select.appendChild(option);
    }

    const autoOption = document.createElement("option");
    autoOption.value = "auto";
    autoOption.textContent = t("languageAuto", displayLang);
    select.appendChild(autoOption);

    select.value =
      storedLanguage in SUPPORTED_LANGUAGES || storedLanguage === "auto"
        ? storedLanguage
        : "en";
  }

  function init(onReady) {
    chrome.storage.sync.get(DEFAULT_SETTINGS, (items) => {
      const lang = resolveLanguage(items.language);
      applyTranslations(lang);
      onReady?.(lang, items);
    });
  }

  function getCurrentLang() {
    return currentLang;
  }

  return {
    SUPPORTED_LANGUAGES,
    resolveLanguage,
    t,
    applyTranslations,
    populateLanguageSelect,
    init,
    getCurrentLang,
  };
